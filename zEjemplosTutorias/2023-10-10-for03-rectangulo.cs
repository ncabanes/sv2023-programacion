using System;

class For03Rectangulo 
{
    static void Main() 
    {
        for (int fila = 1; fila <= 5; fila++)
        {
            for (int columna = 1; columna <= 8; columna++)
            {
                Console.Write("*");
            }
            Console.WriteLine();
        }
    }
}

