/*
Muestra los números del 10 al 20, ambos inclusive, descendiendo, 
separados por un espacio, sin avanzar de línea, usando "for".
*/

// Andrea (...)

using System;

class Del10al20 
{
    static void Main() 
    {
        for(int i = 10; i <= 20; i++)
        {
            Console.Write(i + " ");
        }
    }
}
